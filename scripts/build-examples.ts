/**
 * `scripts/build-examples.ts` — regenerates the 12 example files
 * under `examples/` deterministically.
 *
 *   examples/flowchart/flowchart.{svg,html,mermaid}
 *   examples/sequence/sequence.{svg,html,mermaid}
 *   examples/erd/erd.{svg,html,mermaid}
 *   examples/architecture/architecture.{svg,html,mermaid}
 *
 * Each example is a complete end-to-end render via `make_diagram`
 * (parse → layout → render to SVG/Mermaid/HTML). The DSL specs are
 * defined inline below so the build is fully self-contained — no
 * external fixtures, no LLM calls, no I/O outside the `examples/`
 * directory.
 *
 * Determinism: the renderer is pure (see AC-7), the parser is pure
 * (see AC-6), and the layout engine is pure (see AC-12). Two runs
 * of this script on the same source tree produce byte-identical
 * outputs.
 *
 * Usage: `npm run build:examples`  (alias for `tsx scripts/build-examples.ts`)
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';
import { __INTERNAL__ as make } from '../src/tools/make-diagram.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const EXAMPLES_ROOT = path.resolve(__dirname, '..', 'examples');

interface ExampleSpec {
  /** Directory under `examples/` (also the base name for the 3 files). */
  readonly subdir: 'flowchart' | 'sequence' | 'erd' | 'architecture';
  /** The DSL spec fed to `make_diagram`. */
  readonly spec: string;
  /** The diagram type hint (for `make_diagram`'s `type` arg). */
  readonly type: 'flowchart' | 'sequence' | 'erd';
  /** Theme to render with (every example uses light for byte-stability). */
  readonly theme: 'light';
}

