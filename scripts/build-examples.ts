async function writeExample(spec: ExampleSpec): Promise<void> {
  const dir = path.join(EXAMPLES_ROOT, spec.subdir);
  mkdirSync(dir, { recursive: true });
  const result = await make.makeImpl({ text: spec.spec, type: spec.type, theme: spec.theme });
  const base = path.join(dir, spec.subdir);
  writeFileSync(`${base}.svg`, result.svg, 'utf8');
  writeFileSync(`${base}.html`, result.html, 'utf8');
  writeFileSync(`${base}.mermaid`, result.mermaid, 'utf8');
  // One-line progress output — keeps `npm run build:examples` informative
  // without leaking the full SVG into the build log.
  process.stdout.write(`[build-examples] ${spec.subdir}: ${result.summary}\n`);
}

async function main(): Promise<void> {
  mkdirSync(EXAMPLES_ROOT, { recursive: true });
  for (const spec of EXAMPLES) {
    await writeExample(spec);
  }
  process.stdout.write(`[build-examples] wrote ${EXAMPLES.length * 3} files under ${EXAMPLES_ROOT}\n`);
}

main().catch((err: unknown) => {
  const msg = err instanceof Error ? (err.stack ?? err.message) : String(err);
  process.stderr.write(`[build-examples] fatal: ${msg}\n`);
  process.exit(1);
});
